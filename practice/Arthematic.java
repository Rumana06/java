//arthematic operator +,-,*,/,%(modulus)

class Arthematic
{
    public static void main(String[] args)
    {
        int x,y,z,m,n,p,q;
        x=10;
        y=3;
        z=x+y;
        m=x-y;
        n=x*y;
        p=x/y;
        q=x%y;

        System.out.println(z+"  "+m+"  "+n+" "+p+" "+q);
    }
}